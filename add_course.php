<?php
require 'includes/db.php';

if ($_SERVER['REQUEST_METHOD'] === 'POST') {
    $title = $_POST['title'];
    $video_path = $_POST['video_path'];

    $stmt = $pdo->prepare("INSERT INTO courses (title, video_path) VALUES (?, ?)");
    $stmt->execute([$title, $video_path]);

    header("Location: index.php");
    exit;
}
?>
